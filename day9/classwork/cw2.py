# 2) დაწერეთ მონაცემთა ტიპის შემცვლელი ყველა ფუნქციის მაგალითი

# int() → გარდაქმნის ინტეჯერად
print(int(5.9))        # 5
print(int("10"))       # 10

# float() → გარდაქმნის ათწილადად
print(float(5))        # 5.0
print(float("3.14"))   # 3.14

# str() → გარდაქმნის ტექსტად
print(str(10))         # "10"
print(str(3.5))        # "3.5"

# bool() → გარდაქმნის ლოგიკურ მნიშვნელობად
print(bool(1))         # True
print(bool(0))         # False
print(bool(""))        # False
print(bool("hello"))   # True
