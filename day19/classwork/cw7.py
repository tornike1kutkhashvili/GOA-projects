# 7)მომხმარებელს შეეკითხეთ მისი გვარი, შემდეგ კითხეთ სურს თუ არა გვარის შემობრუნება და თუ დაგიბრუნათ "yes" მაშინ დაუბეჭდეთ მისი გვარი შემობრუნებული, თუ დაგიბრუნათ "no", მაშინ დაუბეჭდეთ მისი გვარი ჩვეულებრივად.

surname = input("შეიყვანე შენი გვარი: ")
answer = input("გსურს გვარის შემობრუნება? (yes/no): ")

if answer == "yes":
    print(surname[::-1])
else:
    print(surname)
